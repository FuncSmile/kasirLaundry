    private void BHitungActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BHitungActionPerformed
        if (isEmpty(tNamaPemesan, "Nama") || isEmpty(tBerat, "Berat")) {
            return;
        }
        double TotalAkhir = 0;
        double diskon = 0;
        double hargaPerKg = 0;

        String layanan = ComboBoxLayanan.getSelectedItem().toString();
        DecimalFormat formatRupiah = new DecimalFormat("Rp #,##0");
        double berat = Double.parseDouble(tBerat.getText());

        switch (layanan) {
            case "cuci", "kering" ->
                hargaPerKg = 8000;
            case "cuci + kering" ->
                hargaPerKg = 10000;
            case "Gosok" ->
                hargaPerKg = 9000;
            case "cuci + kering + gosok" ->
                hargaPerKg = 12000;
        }

        double diskonPersen = 0;

        if ((layanan.equals("cuci") || layanan.equals("kering")) && berat > 3) {
            diskonPersen = 0.10;
        } else if (layanan.equals("cuci + kering") && berat > 5) {
            diskonPersen = 0.15;
        } else if (layanan.equals("Gosok") && berat > 4) {
            diskonPersen = 0.10;
        } else if (layanan.equals("cuci + kering + gosok")) {
            if (berat > 8) {
                diskonPersen = 0.25;
            } else if (berat > 5) {
                diskonPersen = 0.20;
            }
        }

        double total = hargaPerKg * berat;
        diskon = total * diskonPersen;
        TotalAkhir = total - diskon;

        jLabelHarga.setText(formatRupiah.format(hargaPerKg));
        jLabelDiskon1.setText(formatRupiah.format(diskon));
        jLabelTotal.setText(formatRupiah.format(TotalAkhir));

    }//GEN-LAST:event_BHitungActionPerformed

    private void BSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BSimpanActionPerformed
        if (jLabelTotal.getText().equals("0") || jLabelHarga.getText().equals("0")) {
            JOptionPane.showMessageDialog(this,
                    "Silakan klik tombol 'Hitung' terlebih dahulu sebelum menyimpan!",
                    "Data Belum Lengkap",
                    JOptionPane.WARNING_MESSAGE
            );
            return;
        }

        String nama = tNamaPemesan.getText();
        String layanan = ComboBoxLayanan.getSelectedItem().toString();
        double berat = Double.parseDouble(tBerat.getText());
//        double hargaPerKg = Double.parseDouble(jLabelHarga.getText());
        String totalText = jLabelTotal.getText()
                .replace("Rp", "") // hapus simbol
                .replace(",", "") // hapus ribuan
                .trim();            // buang spasi

        double total = Double.parseDouble(totalText);

        // Simpan ke list
        daftar.add(new TempStorage(no_urut, nama, layanan, berat, total));

        // Tambah no urut buat transaksi berikutnya
        no_urut++;

        daftar.forEach(t -> {
            System.out.println(t); // toString() udah di-override
        });

        tampilkanKeTableHistory();

        tNamaPemesan.setText("");
        tBerat.setText("");
        ComboBoxLayanan.setSelectedIndex(4);
        jLabelHarga.setText("10000000");
        jLabelDiskon1.setText("0");
        jLabelTotal.setText("0");
    }//GEN-LAST:event_BSimpanActionPerformed

