    private void tampilkanKeTableHistory() {
        tableModel.setRowCount(0); // bersihin baris sebelum nambah ulang

        for (TempStorage t : daftar) {
            Object[] row = {
                t.getNoUrut(),
                t.getNama(),
                t.getBerat(),
                t.getTotalHarga()
            };
            tableModel.addRow(row);
        }
    }

    private void hitungRingkasanHistory() {
        // init total seluruh harga
        double totalSeluruhHarga = 0;
        // ambil semua daftar yang di simpan
        int totalTransaksi = daftar.size();
        // format harga
        DecimalFormat formatRupiah = new DecimalFormat("Rp #,##0");
        // format label banyak orang
        DecimalFormat formatPelanggan = new DecimalFormat("# orang");

        for (TempStorage t : daftar) {
            totalSeluruhHarga += t.getTotalHarga();
        }
        jLabelPemasukan.setText(formatRupiah.format(totalSeluruhHarga));
        jLabelBPElanggan.setText(formatPelanggan.format(totalTransaksi));
    }

